-(void)setDetails:(NSDictionary *)details{
    dataSource = [NSMutableArray array];
    for (NSString *key in details){
        [dataSource addObject:[NSString stringWithFormat:@"%@:",key]];
        [dataSource addObject:[NSString stringWithFormat:@"%@",[details objectForKey:key]]];
    }
    [self.tableView reloadData];
}

